package E10OO;

public class Fox {
  private String name;
  private String type;
  private String colour;

  Fox(String name, String type, String colour) {
    this.name = name;
    this.type = type;
    this.colour = colour;
  }

  public String getName() {
    return name;
  }

  public String getType() {
    return type;
  }

  public String getColour() {
    return colour;
  }

}
