public class Swap {
    public static void main(String[] args) {
        // Swap the values of the variables
        int a;
        a = 123;

        int b;
        b = 526;

         int c;
         c = a;

         a = b;
         b = c;

        System.out.println(a);
        System.out.println(b);
    }
}